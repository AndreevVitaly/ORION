def validate_contours(contours: dict, vertex_count: int) -> None:
    """Проверить именованные последовательности индексов топологии."""
    if not isinstance(contours, dict):
        raise TypeError("Контуры должны быть переданы словарем")
    for name, indexes in contours.items():
        if not isinstance(name, str) or not isinstance(indexes, list):
            raise TypeError("Контур должен иметь формат имя -> список индексов")
        if len(indexes) < 2:
            raise ValueError(f"Контур {name} содержит слишком мало вершин")
        if any(
            not isinstance(index, int) or index < 0 or index >= vertex_count
            for index in indexes
        ):
            raise ValueError(f"Контур {name} выходит за границы сетки")
